#!/usr/bin/env node
/**
 * Factory pilot static smoke — no DB or server required.
 * Verifies AuditOS 2.0 factory artifacts, flags, routes, and docs.
 */
import { access, readFile } from "node:fs/promises";
import { join } from "node:path";

const root = process.cwd();
const errors = [];
const passed = [];

async function mustExist(relPath, label) {
  try {
    await access(join(root, relPath));
    passed.push(`${label}: ${relPath}`);
  } catch {
    errors.push(`Missing ${label}: ${relPath}`);
  }
}

async function mustContain(relPath, needle, label) {
  const content = await readFile(join(root, relPath), "utf8");
  if (!content.includes(needle)) {
    errors.push(`${label}: "${needle}" not found in ${relPath}`);
  } else {
    passed.push(`${label}: ${relPath}`);
  }
}

async function main() {
  console.log("\nAuditOS Factory — Static Pilot Smoke");
  console.log("=".repeat(44));

  const modules = [
    ["src/lib/audit/fs-engine/fs-rebuild-service.ts", "FS Engine v2"],
    ["src/lib/audit/reconciliation/reconciliation-engine.ts", "Reconciliation"],
    ["src/lib/audit/rules/ifrs-rules-engine.ts", "IFRS Rules"],
    ["src/lib/audit/rules/socpa-rules-engine.ts", "SOCPA Rules"],
    ["src/lib/audit/notes/disclosure-auto.ts", "Disclosure Auto"],
    ["src/lib/audit/intelligence/intelligence-engine.ts", "Intelligence"],
    ["src/lib/audit/governance/approval-gates.ts", "Approval Gates"],
    ["src/lib/audit/reporting-graph/graph-sync-service.ts", "Reporting Graph"],
    ["src/lib/audit/lead-schedule/lead-schedule-generator.ts", "Lead Schedules"],
  ];

  for (const [path, label] of modules) {
    await mustExist(path, label);
  }

  const ui = [
    ["src/components/audit/rules/ifrs-rules-panel.tsx", "IFRS panel"],
    ["src/components/audit/rules/socpa-rules-panel.tsx", "SOCPA panel"],
    ["src/components/audit/notes/disclosure-auto-panel.tsx", "Disclosure panel"],
    ["src/components/audit/reconciliation/reconciliation-panel.tsx", "Reconciliation panel"],
    ["src/app/audit/engagements/[engagementId]/factory-map/page.tsx", "Factory map route"],
  ];

  for (const [path, label] of ui) {
    await mustExist(path, label);
  }

  const flags = [
    "FF_AUDIT_FS_V2",
    "FF_AUDIT_IFRS_RULES",
    "FF_AUDIT_SOCPA_RULES",
    "FF_AUDIT_DISCLOSURE_AUTO",
    "FF_AUDIT_RECONCILIATION",
    "FF_AUDIT_LEAD_SCHEDULE_AUTO",
    "FF_AUDIT_REPORTING_GRAPH",
    "FF_AUDIT_INTELLIGENCE",
    "FF_AUDIT_APPROVAL_GATES",
    "FF_AUDIT_MIND_MAP",
  ];

  for (const flag of flags) {
    await mustContain(".env.example", flag, `Flag ${flag}`);
  }

  await mustContain(
    "src/lib/platform/feature-flags/registry.ts",
    "audit.disclosure-auto",
    "Disclosure auto flag registry",
  );

  await mustContain(
    "src/lib/audit/db/index.ts",
    "maybeAutoGenerateDisclosureNotes",
    "Disclosure auto DB hook",
  );

  const docs = [
    ["docs/systems/auditos/FS_ENGINE.md", "FS doc"],
    ["docs/systems/auditos/IFRS_RULES_ENGINE.md", "IFRS doc"],
    ["docs/systems/auditos/SOCPA_RULES_ENGINE.md", "SOCPA doc"],
    ["docs/systems/auditos/DISCLOSURE_AUTO_ENGINE.md", "Disclosure doc"],
    ["docs/systems/auditos/FACTORY_PILOT_READINESS.md", "Pilot readiness"],
    ["docs/systems/auditos/CURRENT_STATE.md", "Current state"],
  ];

  for (const [path, label] of docs) {
    await mustExist(path, label);
  }

  await mustContain(
    "prisma/seed-audit.ts",
    "eng-gulf-2025",
    "SAR seed engagement",
  );

  console.log(`\n  Passed: ${passed.length}`);
  if (errors.length) {
    console.log(`  Failed: ${errors.length}`);
    for (const e of errors) console.log(`  ❌ ${e}`);
    process.exit(1);
  }
  console.log("  ✅ All static factory pilot checks passed\n");
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
