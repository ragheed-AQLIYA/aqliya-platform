function def(cfg) {
  const prefix = cfg.slug.replace(/-/g, "");
  const rules = cfg.rules.map((r, i) => ({
    ruleId: `${cfg.slug}-r${String(i + 1).padStart(3, "0")}`,
    paragraphReference: r[0],
    ruleText: r[1],
    topic: r[2],
    usage: r[3] ?? "mapping-engine",
  }));
  return {
    slug: cfg.slug,
    standardCode: cfg.code,
    standardName: cfg.name,
    sourceUrl: ifrsUrl(cfg.urlSlug ?? cfg.slug),
    versionLabel: `${cfg.code}:2024`,
    issueDate: cfg.issue ?? "2000-01-01",
    effectiveDate: cfg.effective ?? "2005-01-01",
    assetId: `kf-accounting-a-${cfg.slug}`,
    entityType: cfg.code.startsWith("IFRIC") ? "interpretation" : "standard",
    rules,
    guidance: cfg.guidance ?? [],
  };
}

